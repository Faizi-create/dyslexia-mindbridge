export const STRINGS: Record<Language, typeof en> = { en, ur };

/** Replaces {name} placeholders. Pass the lang explicitly so this is pure. */
export function tFor(lang: Language, key: StringKey, vars?: Record<string, string | number>): string {
  const raw = STRINGS[lang][key] ?? STRINGS.en[key] ?? key;
  if (!vars) return raw;
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v)),
    raw,
  );
}
